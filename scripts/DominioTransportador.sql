CREATE DATABASE Transportadores;
GO

USE Transportadores

GO
CREATE TABLE CodigoDane (
idCodDane [varchar](255) NOT NULL,
municipioDepartameto [varchar](255) NOT NULL,
);

GO
CREATE TABLE Transportador (
Id INT NOT NULL IDENTITY,
Nombre NVARCHAR(90),
EMail NVARCHAR(50),
Direccion NVARCHAR(250),
Telefono NVARCHAR(30),
Estado BIT,
FactorFlete float,
);

GO
CREATE TABLE Cotizacion (
	Id bigint NOT NULL IDENTITY,
	IdTransportador bigint NOT NULL,
	IdCliente bigint NOT NULL,
	CodCiudadOrigen varchar(255)  NOT NULL,
	CodCiudadDestino varchar(255)   NOT NULL,
	Alto float NOT NULL,
	Ancho float NOT NULL,
	Largo float NOT NULL,
	Peso float NOT NULL,
	ValorDeclarado float NOT NULL,
	DestinoNacional bit NOT NULL,
	FechaPeticion datetime NOT NULL,
	Moneda varchar(255),
	ValorCotizacion float ,
	
);
GO

CREATE TABLE Flete (
    IdDaneDepartamento varchar(255) NOT NULL,
    FactorOrigen float ,
    FactorDestino float ,
    FactorSocial float,
    FactorTipoCarga float,
    FactorTipoTransporte float,
    FactorInternacional float ,
    FactorValorCarga float,
    EstadoFlete bit,
);

